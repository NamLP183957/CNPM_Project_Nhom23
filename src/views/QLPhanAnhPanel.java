    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if(!qlPhanAnh.getId()){
            JOptionPane.showMessageDialog(this, "bạn chưa nhập đủ thông tin!","cảnh báo",JOptionPane.WARNING_MESSAGE);
        }else{
            qlPhanAnh.deletePhanAnh();
            JOptionPane.showMessageDialog(this, "successfully!","ok",JOptionPane.OK_OPTION);
            
            typeComboBox.removeAllItems();
            typeComboBox.setSelectedIndex(-1);
            typeComboBox.setSelectedIndex(-1);
            contentTextpane.setText("");
            nameTextFiled.setText("");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

