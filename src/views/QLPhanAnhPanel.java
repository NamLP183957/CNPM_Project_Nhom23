    private void seachButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seachButtonActionPerformed
        // TODO add your handling code here:
        if(nameTextFiled.getText().equals("")){
            JOptionPane.showMessageDialog(this, "bạn chưa nhập tên!","cảnh báo",JOptionPane.WARNING_MESSAGE);
        }else{
            switch(qlPhanAnh.setName(nameTextFiled.getText())){
                case -1:
                    JOptionPane.showMessageDialog(this, "kết nối thất bại","cảnh báo",JOptionPane.WARNING_MESSAGE);
                    return;
                case 0:
                    JOptionPane.showMessageDialog(this, "tên nhập không tồn tại \n yêu cầu nhập lại","cảnh báo",JOptionPane.WARNING_MESSAGE);
                    return;
                case 1:
                    JOptionPane.showMessageDialog(this, "tìm kiếm thành công","ok",JOptionPane.OK_OPTION);
                    qlPhanAnh.pretareTypeList();
                    ArrayList<String> typeList = qlPhanAnh.getTypeList();
                    
                    typeComboBox.removeAllItems();
                    typeComboBox.setSelectedIndex(-1);
                    DefaultComboBoxModel<String> typeModel = (DefaultComboBoxModel<String>) typeComboBox.getModel();
                    
                    for(String s:typeList){
                    typeModel.addElement(s);
                    }
                    typeComboBox.setSelectedIndex(-1);
                    stateComboBox.setSelectedIndex(-1);
                    stateComboBox.removeAllItems();
                    
                    dateComboBox.setSelectedIndex(-1);
                    dateComboBox.removeAllItems();
                    
            }
        }
    }//GEN-LAST:event_seachButtonActionPerformed

    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        // TODO add your handling code here:
        if(dateComboBox.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this, "bạn chưa nhập đủ thông tin","error",JOptionPane.ERROR_MESSAGE);
            
        }else{
            qlPhanAnh.getId();
            contentTextpane.setText(qlPhanAnh.getContent());
            JOptionPane.showMessageDialog(this, "successfully","ok",JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_viewButtonActionPerformed

    private void typeComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_typeComboBoxActionPerformed
        // TODO add your handling code here:
        if(typeComboBox.getSelectedIndex()==-1){
            stateComboBox.setSelectedIndex(-1);
            stateComboBox.removeAllItems();
            stateComboBox.setSelectedIndex(-1);
            dateComboBox.setSelectedIndex(-1);
            dateComboBox.removeAllItems();
            dateComboBox.setSelectedIndex(-1);
        }else{
            qlPhanAnh.setType((String)typeComboBox.getSelectedItem());
            if(qlPhanAnh.prepareStateList()){
            ArrayList<String> stateList = qlPhanAnh.getStateList();
            stateComboBox.removeAllItems();
            DefaultComboBoxModel<String> stateModel =(DefaultComboBoxModel<String>) stateComboBox.getModel();
            stateComboBox.setSelectedIndex(-1);
            
            for(String s:stateList){
                stateModel.addElement(s);
            }
            stateComboBox.setSelectedIndex(-1);
            }else{
                JOptionPane.showMessageDialog(this, "kết nối thất bại","no connection",JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_typeComboBoxActionPerformed

    private void stateComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stateComboBoxActionPerformed
        // TODO add your handling code here:
        if(stateComboBox.getSelectedIndex()==-1){
            dateComboBox.setSelectedIndex(-1);
            dateComboBox.removeAllItems();
            dateComboBox.setSelectedIndex(-1);
        }else{
            qlPhanAnh.setState((String)stateComboBox.getSelectedItem());
            qlPhanAnh.prepareDateList();
            ArrayList<String> dateList = qlPhanAnh.getDateList();
            DefaultComboBoxModel<String> dateModel =(DefaultComboBoxModel<String>) dateComboBox.getModel();
            dateComboBox.setSelectedIndex(-1);
            dateComboBox.removeAllItems();
            for(String s:dateList){
                dateModel.addElement(s);
            }
            dateComboBox.setSelectedIndex(-1);
            
            
        }
    }//GEN-LAST:event_stateComboBoxActionPerformed

